async function main() {
  await mkdir(CACHE_DIR, { recursive: true });
  await mkdir(API_CACHE_DIR, { recursive: true });
  translationCache = await readJson(TRANSLATION_CACHE_FILE, {});

  const [blogCategories, projectTypes, posts, projects, wxrContentByPostId] = await Promise.all([
    fetchTermMapCached(`${BLOG_API}/categories?per_page=100`, "blog-categories"),
    fetchTermMapCached(
      `${PORTFOLIO_API}/jetpack-portfolio-type?per_page=100`,
      "project-types",
    ),
    fetchAllCached(`${BLOG_API}/posts`, [
      "_fields=id,slug,date,modified,title,excerpt,content,link,categories,tags,featured_media",
    ], "blog-posts"),
    fetchAllCached(`${PORTFOLIO_API}/jetpack-portfolio`, [
      "_fields=id,slug,date,modified,title,excerpt,content,link,featured_media,jetpack-portfolio-type",
    ], "portfolio-projects"),
    loadWxrContentMap(),
  ]);

  console.log(`Fetched ${posts.length} blog posts and ${projects.length} portfolio entries.`);

  await resetOutput();

  const legacyPathByPostId = new Map(
    posts.map((post) => [String(post.id), legacyInfo(post.link).path]),
  );

  const blogMediaApi = `${BLOG_API}/media`;
  for (const post of posts) {
    await writeBlogPost(post, blogCategories, blogMediaApi, legacyPathByPostId, wxrContentByPostId);
  }

  const projectMediaApi = `${PORTFOLIO_API}/media`;
  for (const project of projects) {
    await writeProject(project, projectTypes, projectMediaApi, legacyPathByPostId);
  }

  await writeFile(TRANSLATION_CACHE_FILE, `${JSON.stringify(translationCache, null, 2)}\n`);
  console.log("Migration finished.");
}

