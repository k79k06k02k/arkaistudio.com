async function writeBlogPost(post, categoryMap, mediaApi, legacyPathByPostId, wxrContentByPostId) {
  const originalTitle = decodeHtml(post.title?.rendered ?? `Post ${post.id}`);
  const title = await translateText(originalTitle);
  const legacy = legacyInfo(post.link);
  const categories = (post.categories ?? []).map((id) => categoryMap.get(id)).filter(Boolean);

  const sourceHtml = wxrContentByPostId.get(String(post.id)) ?? post.content?.rendered ?? "";
  const cleaned = await prepareHtml(
    sourceHtml,
    "blog",
    mediaApi,
    post.featured_media,
    legacyPathByPostId,
  );
  const translatedHtml = await translateHtml(cleaned.html);
  const body = normalizeMarkdown(turndown.turndown(translatedHtml));
  const description = await buildDescription(post.excerpt?.rendered, translatedHtml, title);
  const heroImage = cleaned.featuredImage ?? firstImageFromMarkdown(body);
  const dateParts = datePartsFromIso(post.date);
  const filename = `${dateParts.year}-${dateParts.month}-${dateParts.day}-${safeFilename(title, post.id)}.md`;

  const frontmatter = toFrontmatter({
    title,
    originalTitle,
    description,
    pubDate: post.date,
    modDate: post.modified,
    legacyPath: legacy.path,
    legacySlug: legacy.slug,
    wpId: String(post.id),
    legacyAliases: buildPostAliases(post.id, legacy.slug, categories),
    categories,
    tags: [],
    sourceUrl: post.link,
    heroImage,
    draft: false,
  });

  await writeFile(
    path.join(BLOG_DIR, filename),
    `---\n${frontmatter}---\n\n<!-- ${CONTENT_WARNING} -->\n\n${body}\n`,
  );
  console.log(`Blog: ${title}`);
}

