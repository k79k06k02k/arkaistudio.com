async function prepareHtml(html, assetScope, mediaApi, featuredMediaId, legacyPathByPostId) {
  const dom = new JSDOM(`<main>${preprocessWordPressHtml(html)}</main>`);
  const document = dom.window.document;
  cleanDocument(document);
  dedupePreTitles(document);
  rewriteInternalLinks(document, legacyPathByPostId);

  const images = [];
  for (const img of [...document.querySelectorAll("img")]) {
    const src = img.getAttribute("src");
    if (!src) continue;
    const local = await downloadMedia(src, assetScope);
    if (!local) {
      img.remove();
      continue;
    }
    img.setAttribute("src", local);
    img.removeAttribute("srcset");
    img.removeAttribute("sizes");
    img.removeAttribute("width");
    img.removeAttribute("height");
    images.push(local);
  }

  let featuredImage;
  if (featuredMediaId) {
    try {
      const featured = await fetchJsonCached(
        `${mediaApi}/${featuredMediaId}?_fields=source_url`,
        `media-${featuredMediaId}-${hashString(mediaApi)}`,
      );
      if (featured?.source_url) {
        featuredImage = await downloadMedia(featured.source_url, assetScope);
        if (featuredImage && !images.includes(featuredImage)) images.unshift(featuredImage);
      }
    } catch (error) {
      console.warn(`Featured media lookup failed; continuing without featured media ${featuredMediaId}.`);
      console.warn(error.message);
    }
  }

  for (const iframe of [...document.querySelectorAll("iframe")]) {
    iframe.setAttribute("loading", "lazy");
    if (!iframe.getAttribute("title")) iframe.setAttribute("title", "Embedded media");
  }

  return {
    html: document.querySelector("main")?.innerHTML ?? "",
    images,
    featuredImage,
  };
}

async function loadWxrContentMap() {
  try {
    const xml = await readFile(WXR_FILE, "utf8");
    const map = new Map();
    for (const item of xml.matchAll(/<item>([\s\S]*?)<\/item>/g)) {
      const block = item[1];
      const type = block.match(/<wp:post_type><!\[CDATA\[(.*?)\]\]><\/wp:post_type>|<wp:post_type>(.*?)<\/wp:post_type>/)?.[1]
        ?? block.match(/<wp:post_type><!\[CDATA\[(.*?)\]\]><\/wp:post_type>|<wp:post_type>(.*?)<\/wp:post_type>/)?.[2];
      if (type !== "post") continue;

      const id = block.match(/<wp:post_id>(\d+)<\/wp:post_id>/)?.[1];
      const content = block.match(/<content:encoded><!\[CDATA\[([\s\S]*?)\]\]><\/content:encoded>/)?.[1];
      if (id && content) map.set(id, content);
    }
    console.log(`Loaded ${map.size} original post bodies from WordPress XML backup.`);
    return map;
  } catch (error) {
    console.warn(`WordPress XML backup not available; falling back to REST rendered HTML. ${error.message}`);
    return new Map();
  }
}

function preprocessWordPressHtml(html) {
  return String(html ?? "")
    .replace(/<!--more-->/g, "")
    .replace(/\[embed\]\s*(https?:\/\/[^\]\s]+)\s*\[\/embed\]/g, (_match, url) => {
      const youtubeId = getYouTubeId(url);
      if (!youtubeId) return `<p><a href="${url}">${url}</a></p>`;
      return `<iframe loading="lazy" width="648" height="365" src="https://www.youtube.com/embed/${youtubeId}" allowfullscreen title="Embedded YouTube video"></iframe>`;
    });
}

function getYouTubeId(value) {
  try {
    const url = new URL(value);
    if (url.hostname === "youtu.be") return url.pathname.replace(/^\//, "");
    if (url.hostname.includes("youtube.com")) return url.searchParams.get("v");
    return null;
  } catch {
    return null;
  }
}

function normalizeCodeLanguage(value) {
  const lowered = value.toLowerCase();
  if (!lowered) return "";
  if (lowered === "c#") return "csharp";
  if (lowered === "js") return "javascript";
  if (lowered === "sh") return "bash";
  return lowered.replace(/[^\w-]/g, "");
}

function rewriteInternalLinks(document, legacyPathByPostId) {
  for (const anchor of [...document.querySelectorAll("a[href]")]) {
    const href = anchor.getAttribute("href");
    if (!href) continue;

    let url;
    try {
      url = new URL(href, "https://www.arkaistudio.com");
    } catch {
      continue;
    }

    const ownHost = url.hostname === "arkaistudio.com" || url.hostname === "www.arkaistudio.com";
    if (!ownHost) continue;

    const decoded = decodePath(url.pathname).replace(/\/$/, "");
    const numericPost = decoded.match(/^\/blog\/(\d+)(?:\/|$)/);
    if (numericPost && legacyPathByPostId?.has(numericPost[1])) {
      anchor.setAttribute("href", `${legacyPathByPostId.get(numericPost[1])}/`);
      continue;
    }

    if (decoded === "/blog" || decoded === "/blog/") {
      anchor.setAttribute("href", "/blog/");
      continue;
    }

    if (decoded === "/blog/showcases" || decoded === "/blog/showcases/") {
      anchor.setAttribute("href", "/blog/showcases/");
      continue;
    }

    if (decoded.startsWith("/blog/201") || decoded.startsWith("/blog/showcases/portfolio/")) {
      anchor.setAttribute("href", `${decoded}/`);
    }
  }
}

function buildPostAliases(id, legacySlug, categories) {
  const aliases = new Set([`/blog/${id}/`]);
  for (const category of categories) {
    aliases.add(`/blog/${id}/${slugifyTerm(category)}/`);
    aliases.add(`/blog/${id}/${slugifyTerm(category)}/${legacySlug}/`);
  }
  return [...aliases];
}

function slugifyTerm(value) {
  return String(value)
    .normalize("NFKD")
    .replace(/[^\w\s-]/g, "")
    .trim()
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .toLowerCase();
}

function cleanDocument(document) {
  const selectors = [
    "script",
    "style",
    "noscript",
    ".sharedaddy",
    ".jp-relatedposts",
    ".sd-sharing-enabled",
    ".post-navigation",
    ".nav-links",
    ".comments-area",
  ];
  for (const selector of selectors) {
    for (const node of [...document.querySelectorAll(selector)]) node.remove();
  }

  for (const element of [...document.querySelectorAll("*")]) {
    for (const attr of [...element.attributes]) {
      if (attr.name.startsWith("on")) element.removeAttribute(attr.name);
    }
  }
}

function dedupePreTitles(document) {
  for (const pre of [...document.querySelectorAll("pre[title]")]) {
    const title = pre.getAttribute("title")?.trim();
    if (!title) continue;

    let previous = pre.previousSibling;
    while (previous && previous.nodeType === document.defaultView.Node.TEXT_NODE) {
      if (previous.textContent.trim() === "") {
        const empty = previous;
        previous = previous.previousSibling;
        empty.remove();
        continue;
      }
      break;
    }

    if (!previous) continue;

    if (
      previous.nodeType === document.defaultView.Node.TEXT_NODE &&
      previous.textContent.trim() === title
    ) {
      previous.remove();
      continue;
    }

    if (
      previous.nodeType === document.defaultView.Node.ELEMENT_NODE &&
      previous.textContent.trim() === title
    ) {
      previous.remove();
    }
  }
}

async function translateHtml(html) {
  const dom = new JSDOM(`<main>${html}</main>`);
  const document = dom.window.document;
  const walker = document.createTreeWalker(
    document.querySelector("main"),
    dom.window.NodeFilter.SHOW_TEXT,
    {
      acceptNode(node) {
        const value = node.nodeValue ?? "";
        if (!hasCjk(value.trim())) return dom.window.NodeFilter.FILTER_REJECT;
        if (shouldSkipTranslation(node.parentElement)) {
          return dom.window.NodeFilter.FILTER_REJECT;
        }
        return dom.window.NodeFilter.FILTER_ACCEPT;
      },
    },
  );

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);

  for (const node of nodes) {
    const original = node.nodeValue ?? "";
    const leading = original.match(/^\s*/)?.[0] ?? "";
    const trailing = original.match(/\s*$/)?.[0] ?? "";
    const translated = await translateText(original.trim());
    node.nodeValue = `${leading}${translated}${trailing}`;
  }

  return document.querySelector("main")?.innerHTML ?? "";
}

function shouldSkipTranslation(element) {
  if (!element) return true;
  const skipTags = new Set(["CODE", "PRE", "SCRIPT", "STYLE", "SVG", "IFRAME", "TEXTAREA"]);
  let current = element;
  while (current) {
    if (skipTags.has(current.tagName)) return true;
    const className = String(current.getAttribute("class") ?? "").toLowerCase();
    if (
      className.includes("crayon") ||
      className.includes("syntax") ||
      className.includes("highlight") ||
      className.includes("code")
    ) {
      return true;
    }
    current = current.parentElement;
  }
  return false;
}

async function translateText(text) {
  const normalized = text.replace(/\s+/g, " ").trim();
  if (!normalized || !hasCjk(normalized)) return normalized;
  if (translationCache[normalized]) return translationCache[normalized];

  try {
    const translated = await googleTranslate(normalized);
    translationCache[normalized] = translated;
    await flushTranslationCache();
    await sleep(80);
    return translated;
  } catch (error) {
    console.warn(`Translation failed; keeping original: ${normalized.slice(0, 80)}`);
    console.warn(error.message);
    translationCache[normalized] = normalized;
    await flushTranslationCache();
    return normalized;
  }
}

async function googleTranslate(text) {
  const chunks = chunkText(text, 1200);
  const translated = [];
  for (const chunk of chunks) {
    const url = new URL("https://translate.googleapis.com/translate_a/single");
    url.searchParams.set("client", "gtx");
    url.searchParams.set("sl", "zh-TW");
    url.searchParams.set("tl", "en");
    url.searchParams.set("dt", "t");
    url.searchParams.set("q", chunk);

    const response = await fetch(url, {
      headers: {
        "user-agent": "ARKAI Studio migration script",
      },
    });
    if (!response.ok) {
      throw new Error(`HTTP ${response.status} from translate endpoint`);
    }
    const data = await response.json();
    translated.push((data?.[0] ?? []).map((part) => part[0]).join(""));
  }
  return translated.join(" ").replace(/\s+([,.;:!?])/g, "$1");
}

function chunkText(text, maxLength) {
  if (text.length <= maxLength) return [text];
  const chunks = [];
  let remaining = text;
  while (remaining.length > maxLength) {
    let index = remaining.lastIndexOf(" ", maxLength);
    if (index < maxLength * 0.5) index = maxLength;
    chunks.push(remaining.slice(0, index));
    remaining = remaining.slice(index).trim();
  }
  if (remaining) chunks.push(remaining);
  return chunks;
}

async function buildDescription(excerptHtml, translatedHtml, fallback) {
  const raw = stripHtml(excerptHtml || translatedHtml).replace(/\s+/g, " ").trim();
  const translated = await translateText(raw || fallback);
  return truncate(translated || fallback, 170);
}

async function downloadMedia(sourceUrl, scope) {
  const absoluteUrl = new URL(sourceUrl, "https://www.arkaistudio.com").href;
  if (mediaCache.has(absoluteUrl)) return mediaCache.get(absoluteUrl);

  const url = new URL(absoluteUrl);
  const hash = createHash("sha1").update(absoluteUrl).digest("hex").slice(0, 10);
  const rawBase = decodeURIComponent(path.basename(url.pathname)).replace(/\?.*$/, "");
  const ext = extensionFromPath(rawBase) || ".bin";
  const name = `${safeFilename(rawBase.replace(/\.[^.]+$/, ""), hash)}-${hash}${ext}`;
  const publicPath = `/assets/migrated/${scope}/${name}`;
  const filePath = path.join(ASSET_ROOT, scope, name);

  await mkdir(path.dirname(filePath), { recursive: true });
  const localMedia = await findLocalBackupMedia(absoluteUrl);
  if (localMedia) {
    await copyFile(localMedia, filePath);
    mediaCache.set(absoluteUrl, publicPath);
    return publicPath;
  }

  try {
    const response = await fetchWithRetry(absoluteUrl);
    if (!response.ok) {
      throw new Error(`HTTP ${response.status}`);
    }
    const buffer = Buffer.from(await response.arrayBuffer());
    await writeFile(filePath, buffer);
    mediaCache.set(absoluteUrl, publicPath);
    return publicPath;
  } catch (error) {
    console.warn(`Media download failed; image removed from migrated content: ${absoluteUrl}`);
    console.warn(error.message);
    mediaCache.set(absoluteUrl, null);
    return null;
  }
}

async function findLocalBackupMedia(absoluteUrl) {
  if (!WORDPRESS_BACKUP_ROOT) return null;
  let url;
  try {
    url = new URL(absoluteUrl);
  } catch {
    return null;
  }

  const decodedPath = decodePath(url.pathname);
  const wpContentIndex = decodedPath.indexOf("/wp-content/");
  if (wpContentIndex === -1) return null;

  const relative = decodedPath.slice(wpContentIndex + 1);
  const candidate = path.join(WORDPRESS_BACKUP_ROOT, relative);
  try {
    await access(candidate);
    return candidate;
  } catch {
    return null;
  }
}

function extensionFromPath(name) {
  const ext = path.extname(name).toLowerCase();
  if (!ext) return "";
  return ext.replace(/[^.\w]/g, "");
}

async function fetchAll(endpoint, params = []) {
  const firstUrl = `${endpoint}?per_page=100${params.length ? `&${params.join("&")}` : ""}`;
  const firstResponse = await fetchWithRetry(firstUrl);
  if (!firstResponse.ok) throw new Error(`Failed to fetch ${firstUrl}: HTTP ${firstResponse.status}`);
  const totalPages = Number(firstResponse.headers.get("x-wp-totalpages") ?? "1");
  const items = await firstResponse.json();
  for (let page = 2; page <= totalPages; page += 1) {
    const url = `${endpoint}?per_page=100&page=${page}${params.length ? `&${params.join("&")}` : ""}`;
    items.push(...(await fetchJson(url)));
  }
  return items;
}

async function fetchAllCached(endpoint, params, cacheName) {
  const cacheFile = apiCacheFile(cacheName);
  try {
    const items = await fetchAll(endpoint, params);
    await writeJson(cacheFile, items);
    return items;
  } catch (error) {
    const cached = await readJson(cacheFile, null);
    if (cached) {
      console.warn(`API fetch failed; using cached ${cacheName}.`);
      console.warn(error.message);
      return cached;
    }
    throw error;
  }
}

async function fetchTermMapCached(endpoint, cacheName) {
  const terms = await fetchJsonCached(endpoint, cacheName);
  return new Map((Array.isArray(terms) ? terms : []).map((term) => [term.id, decodeHtml(term.name)]));
}

async function fetchJson(url) {
  const response = await fetchWithRetry(url);
  if (!response.ok) throw new Error(`Failed to fetch ${url}: HTTP ${response.status}`);
  return response.json();
}

async function fetchJsonCached(url, cacheName) {
  const cacheFile = apiCacheFile(cacheName);
  try {
    const data = await fetchJson(url);
    await writeJson(cacheFile, data);
    return data;
  } catch (error) {
    const cached = await readJson(cacheFile, null);
    if (cached) {
      console.warn(`API fetch failed; using cached ${cacheName}.`);
      console.warn(error.message);
      return cached;
    }
    throw error;
  }
}

async function fetchWithRetry(url, attempts = 3) {
  let lastError;
  for (let attempt = 1; attempt <= attempts; attempt += 1) {
    try {
      const response = await fetch(url, {
        headers: {
          "user-agent": "ARKAI Studio migration script",
        },
        signal: AbortSignal.timeout(15000),
      });
      if (response.ok || attempt === attempts) return response;
      lastError = new Error(`HTTP ${response.status}`);
    } catch (error) {
      lastError = error;
    }
    await sleep(250 * attempt);
  }
  throw lastError;
}

function apiCacheFile(cacheName) {
  return path.join(API_CACHE_DIR, `${safeFilename(cacheName, hashString(cacheName))}.json`);
}

async function writeJson(file, data) {
  await mkdir(path.dirname(file), { recursive: true });
  await writeFile(file, `${JSON.stringify(data, null, 2)}\n`);
}

async function flushTranslationCache(force = false) {
  translationUpdates += 1;
  if (!force && translationUpdates % 25 !== 0) return;
  await writeFile(TRANSLATION_CACHE_FILE, `${JSON.stringify(translationCache, null, 2)}\n`);
}

async function readJson(file, fallback) {
  try {
    return JSON.parse(await readFile(file, "utf8"));
  } catch {
    return fallback;
  }
}

function legacyInfo(link) {
  const url = new URL(link);
  const decodedPath = decodePath(url.pathname).replace(/\/$/, "");
  const segments = decodedPath.split("/").filter(Boolean);
  return {
    path: `/${segments.join("/")}`,
    slug: segments.at(-1) ?? "",
  };
}

function decodePath(pathname) {
  return pathname
    .split("/")
    .map((segment) => {
      try {
        return decodeURIComponent(segment);
      } catch {
        return segment;
      }
    })
    .join("/");
}

function datePartsFromIso(value) {
  const date = new Date(value);
  return {
    year: String(date.getFullYear()),
    month: String(date.getMonth() + 1).padStart(2, "0"),
    day: String(date.getDate()).padStart(2, "0"),
  };
}

function toFrontmatter(data) {
  const lines = [];
  for (const [key, value] of Object.entries(data)) {
    if (value === undefined || value === null || value === "") continue;
    if (Array.isArray(value)) {
      lines.push(`${key}:`);
      if (value.length === 0) {
        lines.push("  []");
      } else {
        for (const item of value) lines.push(`  - ${JSON.stringify(item)}`);
      }
      continue;
    }
    if (typeof value === "boolean") {
      lines.push(`${key}: ${value}`);
      continue;
    }
    lines.push(`${key}: ${JSON.stringify(String(value))}`);
  }
  return `${lines.join("\n")}\n`;
}

function normalizeMarkdown(markdown) {
  return String(markdown)
    .split(/(```[\w-]*\n[\s\S]*?\n```)/g)
    .map((part) => {
      if (part.startsWith("```")) return part;
      return part.replace(/\n{3,}/g, "\n\n").replace(/\\\[/g, "[").replace(/\\\]/g, "]");
    })
    .join("")
    .trim();
}

function firstImageFromMarkdown(markdown) {
  const match = markdown.match(/!\[[^\]]*]\(([^)]+)\)/);
  return match?.[1];
}

function stripHtml(html) {
  return new JSDOM(`<main>${html ?? ""}</main>`).window.document.body.textContent ?? "";
}

function decodeHtml(value) {
  const dom = new JSDOM("<textarea></textarea>");
  const textarea = dom.window.document.querySelector("textarea");
  textarea.innerHTML = value ?? "";
  return textarea.value;
}

function hashString(value) {
  return createHash("sha1").update(String(value)).digest("hex").slice(0, 10);
}

function safeFilename(value, fallback) {
  const ascii = String(value)
    .normalize("NFKD")
    .replace(/[^\w\s-]/g, "")
    .trim()
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .toLowerCase()
    .slice(0, 70);
  return ascii || String(fallback);
}

function truncate(value, maxLength) {
  if (value.length <= maxLength) return value;
  return `${value.slice(0, maxLength - 1).trim()}...`;
}

function hasCjk(value) {
  return /[\u3400-\u9fff]/.test(value);
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

